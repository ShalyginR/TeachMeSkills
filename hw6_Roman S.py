# 1. Дан список чисел, отсортированных по возрастанию.
# На вход принимается значение, равное одному из элементов списка.
# Реализовать функцию, выполняющую рекурсивный алгоритм бинарного поиска,
# на выходе программа должна вывести позицию искомого элемента в исходном списке.
def binary_search_recursive(arr: List[int], target: int) -> int:
    # TODO: реализуй рекурсивный бинарный поиск
    def helper(left: int, right: int) -> int:
        if left > right:
             return -1 #элемент не найден
        mid = (left + right) // 2
        if arr[mid] == target: 
             return mid
        elif target < arr[mid]:
             return helper(left, mid - 1)
        else:
             return helper(mid +1, right)
    return helper(0, len(arr) - 1)

assert binary_search_recursive([1, 3, 5, 7, 9], 7) == 3


