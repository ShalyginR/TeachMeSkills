# 5. Программа получает на вход строку – сообщение и указание, что нужно сделать:
# шифровать или дешифровать.
# Реализовать две функции: первая шифрует заданное сообщение шифром Цезаря,
# вторая – расшифровывает. В зависимости от выбора пользователя (шифровать или дешифровать)
# вызывается соответствующая функция, результат выводится в консоль.
def caesar_encrypt(message: str, shift: int) -> str:
    result = ""
    for ch in message:
        if ch.isalpha():  # если буква
            base = ord('a') if ch.islower() else ord('A')  # базовый код
            pos = ord(ch) - base  # позиция в алфавите 0..25
            shifted_pos = (pos + shift) % 26  # сдвиг с циклом
            result += chr(base + shifted_pos)  # символ после сдвига
        else:
            result += ch  # остальные символы без изменений
    return result

def caesar_decrypt(message: str, shift: int) -> str:
    # дешифровка — сдвиг в обратную сторону
    return caesar_encrypt(message, -shift)

assert caesar_decrypt(caesar_encrypt("hello", 3), 3) == "hello"


