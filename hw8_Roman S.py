# ------------------ ЗАДАЧА 1: ИМТ ------------------
# Подсказка:
# Формула ИМТ = вес (кг) / (рост в метрах)^2
# Например: вес = 70, рост = 1.75 -> ИМТ = 22.86
# Диапазоны ИМТ:
# < 18.5 – Недостаточная масса
# 18.5–24.9 – Норма
# 25–29.9 – Избыточная масса
# 30+ – Ожирение

# TODO: реализовать функцию calculate_bmi(), которая:
# - спрашивает у пользователя рост и вес
# - обрабатывает исключения при неверном вводе
# - считает ИМТ и выводит пояснение


def calculate_bmi():
    pass  # TODO: реализуй функцию
    try:
        weight = float(input("Введите вес (в кг): "))
        height = float(input("Введите рост (в метрах): "))

        if weight <= 0 or height <= 0:
            print("Ошибка: вес и рост должны быть положительными числами.")
            return

        bmi = weight / (height ** 2)
        print(f"Ваш ИМТ: {bmi:.2f}")

        if bmi < 18.5:
            print("Недостаточная масса тела")
        elif bmi < 25:
            print("Норма")
        elif bmi < 30:
            print("Избыточная масса тела")
        else:
            print("Ожирение")

    except ValueError:
        print("Ошибка: пожалуйста, введите корректные числовые значения.")

# ---------------- ЗАДАЧА 2: Калькулятор ----------------
# Подсказка:
# Операции: +, -, *, /
# Нужно:
# - получить два числа и операцию от пользователя
# - выполнить вычисление
# - обработать деление на 0 и ValueError

# TODO: реализовать функцию simple_calculator(), которая:
# - запрашивает у пользователя два числа и операцию
# - обрабатывает исключения
# - выводит результат


def simple_calculator():
    pass  # TODO: реализуй функцию
    try:
        a = float(input("Введите первое число: "))
        b = float(input("Введите второе число: "))
        operation = input("Введите операцию (+, -, *, /): ").strip()

        if operation == '+':
            result = a + b
        elif operation == '-':
            result = a - b
        elif operation == '*':
            result = a * b
        elif operation == '/':
            if b == 0:
                print("Ошибка: деление на ноль невозможно!")
                return
            result = a / b
        else:
            print("Ошибка: неизвестная операция.")
            return

        print(f"Результат: {result}")

    except ValueError:
        print("Ошибка: введите корректные числа.")



# ПРОВЕРКА
if __name__ == "__main__":
    print("--- Индекс массы тела ---")
    calculate_bmi()

    print("\n--- Калькулятор ---")
    simple_calculator()


