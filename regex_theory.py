import re

# === Что такое регулярные выражения? ===
# Регулярные выражения (regex, regexp) — мощный инструмент для поиска, замены
# и валидации строк на основе шаблонов.

# В Python для работы с регулярками используется модуль re

# === Основные функции ===
# re.search() — ищет ПЕРВОЕ совпадение с шаблоном
# re.findall() — возвращает СПИСОК всех совпадений
# re.sub() — заменяет шаблон на другую строку
# re.match() — ищет совпадение в НАЧАЛЕ строки
# re.compile() — создаёт объект шаблона, чтобы использовать его повторно

# === Пример: re.search ===
text = "Сегодня 26.06.2025"
match = re.search(r"\d{2}\.\d{2}\.\d{4}", text)
if match:
    print("Найдена дата:", match.group())  # → Найдена дата: 26.06.2025

# === Спецсимволы в шаблонах ===
# .    — любой символ, кроме \n
# \d   — цифра (0–9)
# \D   — НЕ цифра
# \w   — буква, цифра или _
# \W   — всё, кроме \w
# \s   — пробельный символ (включая табуляцию и \n)
# \S   — НЕ пробел

# === Квантификаторы ===
# *    — 0 или больше повторений
# +    — 1 или больше
# ?    — 0 или 1
# {n}      — ровно n раз
# {n,}     — от n раз
# {n,m}    — от n до m раз

# === Пример: re.findall ===
phones = "Мой номер: 89101234567, а у друга: 8-910-987-65-43"
found = re.findall(r"8[-]?\d{3}[-]?\d{3}[-]?\d{2}[-]?\d{2}", phones)
print("Найденные телефоны:", found)
# → ['89101234567', '8-910-987-65-43']

# === Группы () и захват ===
name_string = "Имя: Иван, Фамилия: Петров"
match = re.search(r"Имя: (\w+), Фамилия: (\w+)", name_string)
if match:
    first_name = match.group(1)
    last_name = match.group(2)
    print("Имя:", first_name, "Фамилия:", last_name)
# → Имя: Иван Фамилия: Петров

# === Альтернатива | ===
color = "Цвет: green"
match = re.search(r"(red|green|blue)", color)
if match:
    print("Найден цвет:", match.group())  # → green

# === re.sub() — замена ===
sentence = "Python это супер, потому что Python гибкий."
new_sentence = re.sub(r"Python", "JavaScript", sentence)
print("Результат:", new_sentence)
# → JavaScript это супер, потому что JavaScript гибкий.

# === Пример: валидация email ===
email = "student123@gmail.com"
if re.match(r"^[\w\.-]+@[\w\.-]+\.\w+$", email):
    print("Email корректен")
else:
    print("Email некорректен")

# === re.compile — для повторного использования ===
pattern = re.compile(r"\d+")
print(pattern.findall("В тексте есть 3 числа: 15, 28 и 42"))
# → ['3', '15', '28', '42']

# === Флаги ===
# re.IGNORECASE или re.I — игнорирует регистр
# re.MULTILINE или re.M — ^ и $ работают построчно
# re.DOTALL или re.S — символ . захватывает \n

multiline_text = """Это первая строка.
а это Вторая строка."""

lines = re.findall(r"^[а-яА-Я\s]+", multiline_text, flags=re.MULTILINE | re.IGNORECASE)
print("Строки:", lines)

# === Практика: Проверка ФИО ===
text = "Подсудимая Эверт-Колокольцева Елизавета Александровна"
fio_pattern = r"[А-ЯЁ][а-яё]+(?:-[А-ЯЁ][а-яё]+)?\s[А-ЯЁ][а-яё]+\s[А-ЯЁ][а-яё]+"
print(re.findall(fio_pattern, text))
# → ['Эверт-Колокольцева Елизавета Александровна']

