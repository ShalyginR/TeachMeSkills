"""
Реализовать алгоритм бинарного поиска по сдвинутому списку
отсортированных чисел (например, дан список [5, 6, 7, 1, 2, 3, 4])
"""

def search_rotated_array(nums, target):
    """
    Поиск элемента в сдвинутом отсортированном списке.
    Возвращает индекс target или -1, если не найден.
    """
    left = 0
    right = len(nums) - 1

    while left <= right:
        mid = (left + right) // 2

        if nums[mid] == target:
            return mid

        # проверяем, отсортирована ли левая половина
        if nums[left] <= nums[mid]:
            if nums[left] <= target < nums[mid]:
                right = mid - 1  # ищем в левой части
            else:
                left = mid + 1   # ищем в правой части

        # Иначе правая половина отсортирована
        else:
            if nums[mid] < target <= nums[right]:
                left = mid + 1   # ищем в правой части
            else:
                right = mid - 1  # ищем в левой части

    return -1  # элемент не найден


